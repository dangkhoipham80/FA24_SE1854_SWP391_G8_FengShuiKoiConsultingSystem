class AutoConsultation {
    numbers: number[];
    directions: string[];
    colors: string[];
    shapes: string[];
    shelters: string[];
    animals: string[];

    constructor(
        numbers: number[],
        directions: string[],
        colors: string[],
        shapes: string[],
        shelters: string[],
        animals: string[]
    ) {
        this.numbers = numbers;
        this.directions = directions;
        this.colors = colors;
        this.shapes = shapes;
        this.shelters = shelters;
        this.animals = animals;
    }
}
export default AutoConsultation;
